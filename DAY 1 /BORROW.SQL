CREATE TABLE BORROW(
    borrowId VARCHAR2(20) PRIMARY KEY NOT NULL,
    memberID VARCHAR2(20) NOT NULL,
    CONSTRAINT FK_MEMBER FOREIGN KEY(memberId) REFERENCES MEMBERS(memberId),
    bookID VARCHAR2(20) NOT NULL,
    CONSTRAINT FK_BOOK FOREIGN KEY(bookId) REFERENCES BOOKS(bookId),
    borrowDate VARCHAR2(20) NOT NULL,
    dueDate VARCHAR2(20) NOT NULL,
    returnDate VARCHAR2(20) NOT NULL,
    fineAmt NUMBER 
);
